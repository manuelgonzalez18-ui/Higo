-- Tabla driver_memberships.
CREATE TABLE IF NOT EXISTS public.driver_memberships (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    driver_id      UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    plan           TEXT NOT NULL CHECK (plan IN ('moto','standard','van')),
    amount         NUMERIC NOT NULL,
    period         TEXT NOT NULL DEFAULT 'monthly' CHECK (period IN ('weekly','monthly','yearly')),
    payment_method TEXT NOT NULL DEFAULT 'pago_movil',
    reference      TEXT,
    paid_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    expires_at     TIMESTAMPTZ NOT NULL,
    status         TEXT NOT NULL DEFAULT 'active' CHECK (status IN ('active','expired','refunded')),
    notes          TEXT,
    created_at     TIMESTAMPTZ DEFAULT NOW(),
    created_by     UUID REFERENCES public.profiles(id)
);

CREATE INDEX IF NOT EXISTS idx_memberships_driver
    ON public.driver_memberships(driver_id, expires_at DESC);
CREATE INDEX IF NOT EXISTS idx_memberships_active_expires
    ON public.driver_memberships(expires_at)
    WHERE status = 'active';

ALTER TABLE public.driver_memberships ENABLE ROW LEVEL SECURITY;

