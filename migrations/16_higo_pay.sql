    WITH CHECK (EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin'));

-- Helper: ¿el conductor tiene membresía activa?
CREATE OR REPLACE FUNCTION public.driver_has_active_membership(p_driver_id UUID)
RETURNS BOOLEAN
LANGUAGE SQL
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
    SELECT EXISTS (
        SELECT 1
        FROM public.driver_memberships
        WHERE driver_id = p_driver_id
          AND status = 'active'
          AND expires_at > NOW()
    );
$$;

GRANT EXECUTE ON FUNCTION public.driver_has_active_membership(UUID) TO authenticated;

-- Trigger: sync profiles.subscription_status ← driver_memberships.
CREATE OR REPLACE FUNCTION public.sync_driver_subscription_status()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
    UPDATE public.profiles
       SET subscription_status = CASE
               WHEN public.driver_has_active_membership(NEW.driver_id) THEN 'active'
               ELSE 'suspended'
           END,
           last_payment_date = NEW.paid_at
     WHERE id = NEW.driver_id;
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_sync_subscription_status ON public.driver_memberships;
CREATE TRIGGER trg_sync_subscription_status
    AFTER INSERT OR UPDATE ON public.driver_memberships
    FOR EACH ROW
    EXECUTE FUNCTION public.sync_driver_subscription_status();

